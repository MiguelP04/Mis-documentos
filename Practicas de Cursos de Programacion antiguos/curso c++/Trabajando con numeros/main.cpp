#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    cout << "suma " << 2 + 5 << endl;
    cout << "resta " << 2 - 5 << endl;
    cout << "multiplicacion " << 2 * 5 << endl;
    cout << "division " << 2.0 / 5.0 << endl;
    cout << "residuo " << 2 % 5 << endl;
    cout << "potencia " << pow(2,5) << endl;
    cout << "raiz " << sqrt(25) << endl;
    cout << round(2.5) << endl;
    cout << ceil(2.25) << endl;
    cout << floor(2.75)<< endl;
    return 0;
}
